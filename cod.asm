OUT DX, AL
CALL Delay
MOV AL, 00000110B
OUT DX, AL
CALL Delay
MOV AL, 00001100B
OUT DX, AL
CALL Delay
MOV AL, 00001001B
OUT DX, AL
CALL Delay
RET
Delay:
PUSH CX
MOV CX, 0500h
Delay_Loop:
LOOP Delay_Loop
POP CX
RET

CODE ENDS
END START
