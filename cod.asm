CODE SEGMENT
ASSUME CS:CODE, DS:CODE, SS:CODE, ES:CODE
ORG 100h

START:
DEFINE_PORTS:
PORTA EQU 00H
PORTB EQU 02H
PORTC EQU 04H
Config EQU 06H
MOV DX, Config
MOV AL, 10000000B
OUT DX, AL

MOV CX, 3
CICLO_0:
; Moviendo base a 180 grados
MOV BX, 156
LOOP_GEN_base_180_624:
CALL Motor1_Forward
DEC BX
JNZ LOOP_GEN_base_180_624
; Moviendo cuerpo a 90 grados
MOV BX, 80
LOOP_GEN_cuerpo_90_607:
CALL Motor2_Forward
DEC BX
JNZ LOOP_GEN_cuerpo_90_607
; Moviendo garra a 45 grados
MOV BX, 40
LOOP_GEN_garra_45_677:
CALL Motor3_Forward
DEC BX
JNZ LOOP_GEN_garra_45_677
; Moviendo base a 0 grados
MOV BX, 156
LOOP_GEN_base_0_870:
CALL Motor1_Backward
DEC BX
JNZ LOOP_GEN_base_0_870
; Moviendo cuerpo a 0 grados
MOV BX, 80
LOOP_GEN_cuerpo_0_14:
CALL Motor2_Backward
DEC BX
JNZ LOOP_GEN_cuerpo_0_14
; Moviendo garra a 0 grados
MOV BX, 40
LOOP_GEN_garra_0_453:
CALL Motor3_Backward
DEC BX
JNZ LOOP_GEN_garra_0_453
DEC CX
CMP CX, 0
JE FIN_CICLO_0
JMP CICLO_0
FIN_CICLO_0:

RET

Motor1_Forward:
MOV DX, PORTA
MOV AL, 00001001B
OUT DX, AL
CALL Delay
MOV AL, 00001100B
OUT DX, AL
CALL Delay
MOV AL, 00000110B
OUT DX, AL
CALL Delay
MOV AL, 00000011B
OUT DX, AL
CALL Delay
RET
Motor1_Backward:
MOV DX, PORTA
MOV AL, 00000011B
OUT DX, AL
CALL Delay
MOV AL, 00000110B
OUT DX, AL
CALL Delay
MOV AL, 00001100B
OUT DX, AL
CALL Delay
MOV AL, 00001001B
OUT DX, AL
CALL Delay
RET
Motor2_Forward:
MOV DX, PORTB
MOV AL, 00001001B
OUT DX, AL
CALL Delay
MOV AL, 00001100B
OUT DX, AL
CALL Delay
MOV AL, 00000110B
OUT DX, AL
CALL Delay
MOV AL, 00000011B
OUT DX, AL
CALL Delay
RET
Motor2_Backward:
MOV DX, PORTB
MOV AL, 00000011B
OUT DX, AL
CALL Delay
MOV AL, 00000110B
OUT DX, AL
CALL Delay
MOV AL, 00001100B
OUT DX, AL
CALL Delay
